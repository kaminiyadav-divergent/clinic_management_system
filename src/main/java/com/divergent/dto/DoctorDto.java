package com.divergent.dto;

public class DoctorDto {
	private String did, dname, dspeciality, degree, dfees;

	public String getDid() {
		return did;
	}

	public void setDid(String did) {
		this.did = did;
	}

	public String getDname() {
		return dname;
	}

	public void setDname(String dname) {
		this.dname = dname;
	}

	public String getDspeciality() {
		return dspeciality;
	}

	public void setDspeciality(String dspeciality) {
		this.dspeciality = dspeciality;
	}

	public String getDegree() {
		return degree;
	}

	public void setDegree(String degree) {
		this.degree = degree;
	}

	public String getDfees() {
		return dfees;
	}

	public void setDfees(String dfees) {
		this.dfees = dfees;
	}
}
