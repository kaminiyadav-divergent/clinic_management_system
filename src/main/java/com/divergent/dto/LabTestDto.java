package com.divergent.dto;

public class LabTestDto {
	private String labtest_id, labtest_name, pname, labtest_date, labtest_time;

	public String getLabtest_id() {
		return labtest_id;
	}

	public void setLabtest_id(String labtest_id) {
		this.labtest_id = labtest_id;
	}

	public String getLabtest_name() {
		return labtest_name;
	}

	public void setLabtest_name(String labtest_name) {
		this.labtest_name = labtest_name;
	}

	public String getPname() {
		return pname;
	}

	public void setPname(String pname) {
		this.pname = pname;
	}

	public String getLabtest_date() {
		return labtest_date;
	}

	public void setLabtest_date(String labtest_date) {
		this.labtest_date = labtest_date;
	}

	public String getLabtest_time() {
		return labtest_time;
	}

	public void setLabtest_time(String labtest_time) {
		this.labtest_time = labtest_time;
	}
}
